let sleepTime = null;
let generalCommentTimer = null;

const bedtimeInput = document.getElementById('bedtime');
const sleepBtn = document.getElementById('sleepBtn');
const wakeupBtn = document.getElementById('wakeupBtn');
const commentDiv = document.getElementById('comment');
const statusDiv = document.getElementById('status');

function getRandomComment(list) {
  return list[Math.floor(Math.random() * list.length)];
}

function startGeneralCommentTimer() {
  if (generalCommentTimer) clearInterval(generalCommentTimer);
  generalCommentTimer = setInterval(() => {
    if (!sleepTime) {
      commentDiv.textContent = getRandomComment(generalComments);
    }
  }, 15 * 60 * 1000); // каждые 15 минут
}

sleepBtn.addEventListener('click', () => {
  const bedtime = bedtimeInput.value;
  if (!bedtime) {
    alert('Пожалуйста, выберите время сна!');
    return;
  }
  sleepTime = new Date();
  const [hours, minutes] = bedtime.split(':');
  sleepTime.setHours(hours, minutes, 0, 0);
  commentDiv.textContent = getRandomComment(generalComments);
  statusDiv.textContent = `Время сна установлено на ${bedtime}. Спокойной ночи!`;
});

wakeupBtn.addEventListener('click', () => {
  if (!sleepTime) {
    alert('Ты ещё не ложился спать!');
    return;
  }
  const wakeTime = new Date();
  let sleepDuration = (wakeTime - sleepTime) / 3600000; // в часах
  sleepDuration = Math.round(sleepDuration * 10) / 10; // округляем до 1 знака
  let category = 'bad';
  if (sleepDuration >= 7) category = 'good';
  const comment = (category === 'good' ? morningGoodComments : morningBadComments)[Math.floor(Math.random() * (category === 'good' ? morningGoodComments.length : morningBadComments.length))];
  commentDiv.textContent = comment.replace('%hours%', sleepDuration);
  statusDiv.textContent = `Ты спал: ${sleepDuration} ч.`;
});

window.onload = () => {
  startGeneralCommentTimer();
};
